function collectParallelMessages(english: Messages, arabic: Messages, result: Map<string, string>) {
  for (const [key, englishValue] of Object.entries(english)) {
    const arabicValue = arabic[key];
    if (typeof englishValue === "string" && typeof arabicValue === "string") {
      result.set(englishValue, arabicValue);
    } else if (englishValue && arabicValue && typeof englishValue === "object" && typeof arabicValue === "object") {
      collectParallelMessages(englishValue as Messages, arabicValue as Messages, result);
    }
  }
}

